public class MathMethods {

    public static int sum(int a, int b) {
        return a + b;
    }

    public static int product(int a, int b) {
        return a * b;
    }

    public static void main(String[] args) {
        System.out.println("Sum: " + sum(7, 5));
        System.out.println("Product: " + product(7, 5));
    }
}
